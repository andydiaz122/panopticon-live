function ToolResultEvent({ event }: { event: TraceToolResult }) {
  // GOTCHA-030: event.output_json may carry the backend truncation sentinel
  // " ... [Array truncated for UI playback]" which makes it INVALID JSON.
  // NEVER call JSON.parse() on this value directly — use safeParseOutputJson
  // from @/lib/agentTracePlayback when a parsed form is required. Here we
  // only render as a raw string, which is safe.
  const isTruncated = isTruncatedToolOutput(event.output_json);
  const provenance = event.provenance ?? 'live_anthropic';
  const visibleSlice = event.output_json.length > 500
    ? event.output_json.slice(0, 500) + '…'
    : event.output_json;
  return (
    <div
      className="rounded border px-3 py-2"
      style={{
        background: event.is_error ? `${colors.anomaly}12` : `${colors.playerA}08`,
        borderColor: event.is_error ? colors.anomaly : colors.border,
        fontSize: 12,
      }}
    >
      <span
        className="mono mb-1 flex items-center gap-2 text-[9px] uppercase tracking-[0.16em]"
        style={{ color: event.is_error ? colors.anomaly : colors.playerA }}
      >
        <span>{event.is_error ? 'Tool error' : 'Tool result'} · {event.tool_name}</span>
        <ProvenanceChip tag={provenance} />
        {isTruncated && (
          <span
            className="rounded px-1.5 py-0.5"
            style={{
              background: `${colors.textMuted}22`,
              color: colors.textMuted,
              fontSize: 8,
              letterSpacing: '0.14em',
            }}
            title="Backend trimmed the full payload for UI safety (GOTCHA-027). The LLM saw the complete result during execution."
          >
            Truncated
          </span>
        )}
      </span>
      <code
        className="mono block overflow-x-auto whitespace-pre-wrap break-words"
        style={{ color: colors.textSecondary, fontSize: 11 }}
      >
        {visibleSlice}
      </code>
    </div>
  );
}

