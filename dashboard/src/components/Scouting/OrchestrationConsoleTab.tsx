/**
 * Tab 3 — Live Agent Orchestration Console (PATTERN-056 / USER-CORRECTION-024).
 *
 * Loads `agent_trace.json` (generated by the offline Scouting Committee run
 * during precompute) via `fetch()` in `useEffect` — never as a Server Component
 * prop (GOTCHA-026 hydration death). Plays back the captured event sequence
 * with deterministic pacing so judges physically see the multi-agent
 * reasoning unfold: Analytics Specialist → Technical Coach → Tactical Strategist.
 *
 * The "ARCHITECTURAL PREVIEW: SWARM ACCELERATED FOR DEMO" banner discloses
 * that playback pacing is UI-only — the real reasoning happened offline.
 */
export default function OrchestrationConsoleTab() {
  const { matchData } = usePanopticonState();
  const [trace, setTrace] = useState<AgentTrace | null>(null);
  const [loadError, setLoadError] = useState<string | null>(null);
  const [isPlaying, setIsPlaying] = useState(false);
  const [cursor, setCursor] = useState(0); // index of NEXT item to reveal
  // Fast Forward: 1× is real baud-rate pacing (~25 cps); 4× divides every
  // setTimeout delay by 4 so judges can skim the long text blocks.
  const [playbackSpeed, setPlaybackSpeed] = useState<1 | 4>(1);
  const timerRef = useRef<ReturnType<typeof setTimeout> | null>(null);

  const matchId = matchData?.meta.match_id ?? 'utr_01_segment_a';

  // Load agent_trace.json — client-side fetch, NEVER RSC props (GOTCHA-026)
  useEffect(() => {
    let cancelled = false;
    fetch('/match_data/agent_trace.json')
      .then((r) => {
        if (!r.ok) {
          throw new Error(`agent_trace.json ${r.status}`);
        }
        return r.json() as Promise<AgentTrace>;
      })
      .then((data) => {
        if (!cancelled) setTrace(data);
      })
      .catch((err: unknown) => {
        if (!cancelled) {
          setLoadError(err instanceof Error ? err.message : String(err));
        }
      });
    return () => {
      cancelled = true;
    };
  }, []);

  const items = useMemo<PlaybackItem[]>(
    () => (trace ? flattenTraceForPlayback(trace) : []),
    [trace],
  );
  const totalMs = useMemo(() => (trace ? totalPlaybackDurationMs(trace) : 0), [trace]);

  const revealedItems = items.slice(0, cursor);
  const finished = cursor >= items.length && items.length > 0;

  // Playback driver: advance cursor after each item's delay
  useEffect(() => {
    if (!isPlaying) return;
    if (cursor >= items.length) {
      setIsPlaying(false);
      return;
    }
    const currentItem = items[cursor];
    const prevReveal = cursor === 0 ? 0 : items[cursor - 1].revealAtMs;
    const delayMs = currentItem.revealAtMs - prevReveal;
    // Scale by playback speed — 4× means "skim real reasoning" not "skip to end".
    const scaledDelay = Math.max(1, Math.round(delayMs / playbackSpeed));

    timerRef.current = setTimeout(() => {
      setCursor((c) => c + 1);
    }, scaledDelay);

    return () => {
      if (timerRef.current) clearTimeout(timerRef.current);
    };
  }, [isPlaying, cursor, items, playbackSpeed]);

  const onPlay = useCallback(() => {
    if (cursor >= items.length) setCursor(0); // restart if finished
    setIsPlaying(true);
  }, [cursor, items.length]);
  const onPause = useCallback(() => setIsPlaying(false), []);
  const onSkipToEnd = useCallback(() => {
    setIsPlaying(false);
    setCursor(items.length);
  }, [items.length]);
  const onRestart = useCallback(() => {
    setIsPlaying(false);
    setCursor(0);
    setPlaybackSpeed(1);
  }, []);
  const onToggleSpeed = useCallback(() => {
    setPlaybackSpeed((s) => (s === 1 ? 4 : 1));
  }, []);

  if (loadError) {
    return (
      <TabChrome matchId={matchId}>
        <div
          className="mx-auto w-full max-w-[900px] rounded-xl border p-8 text-[13px]"
          style={{ background: colors.bg1, borderColor: colors.border, color: colors.anomaly }}
        >
          <p className="font-semibold">Could not load agent_trace.json</p>
          <p className="mt-2" style={{ color: colors.textSecondary }}>
            {loadError}
          </p>
          <p className="mt-3" style={{ color: colors.textMuted }}>
            Expected at <code>/match_data/agent_trace.json</code>. Re-run{' '}
            <code>python -m backend.precompute …</code> with an ANTHROPIC_API_KEY to generate it.
          </p>
        </div>
      </TabChrome>
    );
  }

  if (!trace) {
    return (
      <TabChrome matchId={matchId}>
        <div
          className="mx-auto w-full max-w-[900px] rounded-xl border p-8 text-center text-[13px]"
          style={{ background: colors.bg1, borderColor: colors.border, color: colors.textMuted }}
        >
          <span className="mono">Loading committee trace…</span>
        </div>
      </TabChrome>
    );
  }

  const progressPct = totalMs === 0 ? 0 : (revealedItems.length === 0
    ? 0
    : (revealedItems[revealedItems.length - 1].revealAtMs / totalMs) * 100);

  const matchTimecode = formatMatchTimeRange(trace.match_time_range_ms ?? null);

  return (
    <TabChrome matchId={matchId}>
      <div className="mx-auto w-full max-w-[1200px] flex-1 overflow-y-auto">
        <PreviewBanner />
        {matchTimecode && <MatchTimecodeAnchor timecode={matchTimecode} />}
        <CommitteeGoal goal={trace.committee_goal} />
        <TransportBar
          isPlaying={isPlaying}
          finished={finished}
          cursor={cursor}
          total={items.length}
          progressPct={progressPct}
          playbackSpeed={playbackSpeed}
          onPlay={onPlay}
          onPause={onPause}
          onSkipToEnd={onSkipToEnd}
          onRestart={onRestart}
          onToggleSpeed={onToggleSpeed}
        />

        <section className="mt-6 grid grid-cols-1 gap-4 lg:grid-cols-3">
          {trace.steps.map((step, i) => (
            <AgentColumn
              key={`${step.agent_name}-${i}`}
              step={step}
              stepIndex={i}
              revealedItems={revealedItems}
            />
          ))}
        </section>

        {finished && (
          <motion.section
            initial={{ opacity: 0, y: 16 }}
            animate={{ opacity: 1, y: 0 }}
            transition={motionTokens.springStandard}
            className="mx-auto mt-8 max-w-[900px] rounded-xl border p-8"
            style={{ background: colors.bg1, borderColor: colors.border }}
          >
            <header className="mb-4 flex items-center gap-2">
              <span
                className="mono text-[10px] uppercase tracking-[0.18em]"
                style={{ color: colors.opusThinking }}
              >
                Final Tactical Brief
              </span>
            </header>
            <div
              className="scouting-markdown prose prose-invert prose-sm max-w-none"
              style={{ color: colors.textPrimary }}
            >
              <ReactMarkdown>{trace.final_report_markdown}</ReactMarkdown>
            </div>
          </motion.section>
        )}
      </div>
      <ConsoleStyles />
    </TabChrome>
  );
}

