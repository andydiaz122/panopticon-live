function TransportBar({
  isPlaying,
  finished,
  cursor,
  total,
  progressPct,
  playbackSpeed,
  onPlay,
  onPause,
  onSkipToEnd,
  onRestart,
  onToggleSpeed,
}: {
  isPlaying: boolean;
  finished: boolean;
  cursor: number;
  total: number;
  progressPct: number;
  playbackSpeed: 1 | 4;
  onPlay: () => void;
  onPause: () => void;
  onSkipToEnd: () => void;
  onRestart: () => void;
  onToggleSpeed: () => void;
}) {
  return (
    <div className="mx-auto mt-4 flex max-w-[1200px] items-center gap-3">
      {!isPlaying && !finished && (
        <TransportButton onClick={onPlay} accent>
          {cursor === 0 ? '▶  Play Committee' : '▶  Resume'}
        </TransportButton>
      )}
      {isPlaying && (
        <TransportButton onClick={onPause}>⏸  Pause</TransportButton>
      )}
      {finished && (
        <TransportButton onClick={onRestart}>↻  Replay</TransportButton>
      )}
      {!finished && (
        <TransportButton
          onClick={onToggleSpeed}
          active={playbackSpeed === 4}
          title="Accelerate playback — keeps the full trace visible but skims the long text blocks"
        >
          {playbackSpeed === 4 ? '▶  1× Speed' : '»  4× Speed'}
        </TransportButton>
      )}
      {!finished && (
        <TransportButton onClick={onSkipToEnd}>⏭  Skip to brief</TransportButton>
      )}
      <div className="flex-1">
        <div
          className="h-1 rounded-full"
          style={{ background: `${colors.border}` }}
        >
          <div
            className="h-1 rounded-full transition-all duration-200"
            style={{ width: `${progressPct}%`, background: colors.opusThinking }}
          />
        </div>
      </div>
      <span
        className="mono text-[10px] uppercase tracking-[0.16em]"
        style={{ color: colors.textMuted }}
      >
        {cursor} / {total} events{playbackSpeed === 4 ? ' · 4×' : ''}
      </span>
    </div>
  );
}

function TransportButton({
  onClick,
  accent,
  active,
  title,
  children,
}: {
  onClick: () => void;
  accent?: boolean;
  active?: boolean;
  title?: string;
  children: React.ReactNode;
}) {
  // Three visual states:
  //  - accent: primary action (Play)
  //  - active: toggle ON (Fast Forward currently engaged)
  //  - default: secondary action
  const background = accent
    ? colors.opusThinking
    : active
      ? `${colors.opusThinking}22`
      : 'transparent';
  const borderColor = accent || active ? colors.opusThinking : colors.border;
  const color = accent
    ? '#FFFFFF'
    : active
      ? colors.opusThinking
      : colors.textSecondary;
  return (
    <button
      type="button"
      onClick={onClick}
      title={title}
      className="mono rounded border px-3 py-1.5 text-[11px] uppercase tracking-[0.14em] transition-colors"
      style={{ background, borderColor, color }}
    >
      {children}
    </button>
  );
}

function AgentColumn({
  step,
  stepIndex,
  revealedItems,
}: {
  step: (AgentTrace['steps'])[number];
  stepIndex: number;
  revealedItems: ReadonlyArray<PlaybackItem>;
}) {
  const mine = revealedItems.filter((it) => it.stepIndex === stepIndex);
  const isActive = mine.length > 0 && mine.length < step.events.length;
  const isComplete = mine.length >= step.events.length && step.events.length > 0;
  const statusLabel = isComplete ? 'complete' : isActive ? 'active' : 'pending';
  const statusColor = isComplete
    ? colors.playerA
    : isActive
      ? colors.opusThinking
      : colors.textMuted;

  return (
    <div
      className="flex flex-col rounded-xl border p-4"
      style={{
        background: colors.bg1,
        borderColor: isActive ? colors.opusThinking : colors.border,
        boxShadow: isActive ? `0 0 24px ${colors.opusThinking}33` : 'none',
        transition: 'all 250ms ease',
        minHeight: 340,
      }}
    >
      <header className="mb-3 flex items-center justify-between">
        <div className="flex flex-col">
          <span
            className="text-[13px] font-semibold"
            style={{ color: colors.textPrimary }}
          >
            {step.agent_name}
          </span>
          <span
            className="mono text-[10px] uppercase tracking-[0.14em]"
            style={{ color: colors.textMuted }}
          >
            {step.agent_role}
          </span>
        </div>
        <span
          className="mono text-[9px] uppercase tracking-[0.18em]"
          style={{ color: statusColor }}
        >
          {statusLabel}
        </span>
      </header>

      <div className="flex flex-1 flex-col gap-2">
        <AnimatePresence initial={false}>
          {mine.map((it) => (
            <EventPill key={`${stepIndex}-${it.event.t_ms}-${it.event.kind}`} item={it} />
          ))}
        </AnimatePresence>
      </div>
    </div>
  );
}

function EventPill({ item }: { item: PlaybackItem }) {
  return (
    <motion.div
      initial={{ opacity: 0, y: 6 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ duration: 0.25 }}
    >
      {renderEvent(item.event)}
    </motion.div>
  );
}

function renderEvent(event: PlaybackItem['event']) {
  switch (event.kind) {
    case 'thinking':
      return <ThinkingEvent event={event} />;
    case 'tool_call':
      return <ToolCallEvent event={event} />;
    case 'tool_result':
      return <ToolResultEvent event={event} />;
    case 'text':
      return <TextEvent event={event} />;
    case 'handoff':
      return <HandoffEvent event={event} />;
  }
}

